% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
    location = [];
    %标定
    %选取像素，计算实际坐标
    flag = 0;
    while 1
        [x,y] = ginput(1);
        flag = flag + 1;
        if x<0
            break
        end
        location = [location;[x y]];
        set(handles.edit1,'string',num2str(x))
        set(handles.edit2,'string',num2str(y))
        set(handles.edit3,'string',num2str(flag))
        
    end
    formatOut = 'yyyymmddTHHMMSS';
    filename = datestr(now,formatOut)
    set(handles.filename,'userdata',filename)
    set(handles.filename,'string',[filename,'.xls'])
    title = {'X坐标','Y坐标'}
    xlswrite(filename,title)
    sheet = 1;
    xlRange = 'A2';
    xlswrite(filename,location,sheet,xlRange)

